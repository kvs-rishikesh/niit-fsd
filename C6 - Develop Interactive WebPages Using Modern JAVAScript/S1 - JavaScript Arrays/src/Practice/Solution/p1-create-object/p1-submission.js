function displayObjectProperties(){
    const student = {
        firstName: "John",
        lastName: "Britto",
        age: 15,
        email: "john@gmail.com",
        phoneNum: "258963654",
        address: "Park Avenue, New York"
    }
    console.log("First Name: ",student.firstName);
    console.log("Last Name: ",student.lastName);
    console.log("Age: ",student.age);
    console.log("Email: ",student.email);
    console.log("Phone Number: ",student.phoneNum);
    console.log("Address: ",student.address);
}

displayObjectProperties();